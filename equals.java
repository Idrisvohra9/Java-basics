public class equals {
    public static void main(String[] args) {
        Integer i=90,j=40;
        // String a= "hello", b= "world";
        // equals function is used to see if a variable contains a value of other variable or other value.
        // it is mostly used to check strings as they cannot be compared with relational operator
        System.out.println(i.equals(j));
        System.out.println(i.equals(90));
    }
}
